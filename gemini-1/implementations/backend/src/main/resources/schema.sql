CREATE TABLE IF NOT EXISTS users (
     id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
     name VARCHAR(255),
    passwd VARCHAR(255),
    email VARCHAR(255),
    user_type VARCHAR(50) NOT NULL
    );

CREATE TABLE IF NOT EXISTS audit_logs (
    log_id VARCHAR(100) PRIMARY KEY,
    timestamp TIMESTAMP,
    level VARCHAR(50),
    event_type VARCHAR(100),
    message CLOB,
    user_id VARCHAR(100),
    target_id INTEGER
    );

CREATE TABLE IF NOT EXISTS data_processing_requirements (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    file_type VARCHAR(50),
    file_quality VARCHAR(50),
    color_type VARCHAR(50),
    exposure DOUBLE,
    contrast DOUBLE,
    brightness DOUBLE,
    saturation DOUBLE
    );

CREATE TABLE IF NOT EXISTS science_plans (
    plan_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plan_name VARCHAR(255),
    funding DOUBLE,
    objective CLOB,
    start_date TIMESTAMP,
    end_date TIMESTAMP,
    last_modified TIMESTAMP,
    telescope_site VARCHAR(50),
    target_constellation VARCHAR(50),
    state VARCHAR(50),
    creator_id INTEGER,
    submitter_id INTEGER,
    requirements_id BIGINT,

    CONSTRAINT fk_creator FOREIGN KEY (creator_id) REFERENCES users(id),
    CONSTRAINT fk_submitter FOREIGN KEY (submitter_id) REFERENCES users(id),
    CONSTRAINT fk_requirements FOREIGN KEY (requirements_id) REFERENCES data_processing_requirements(id)
    );